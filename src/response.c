void serve_file(int client, char *filename)
{
    FILE *fd = NULL;
    fd = fopen(filename, "r");

    if (!fd) {
        http_not_found(client);
    } else {
        http_ok(client, filename);

        char buf[1024];
        do {
            fgets(buf, sizeof(buf), fd);
            send(client, buf, strlen(buf), 0);
        } while(!feof(fd));
    }
    if (fd)
        fclose(fd);
}

